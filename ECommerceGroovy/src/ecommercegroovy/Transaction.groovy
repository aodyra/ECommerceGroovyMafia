class Transaction
{
  def wholesale

  public Transaction()
  {
    this.wholesale = 0;
  }

  public Transaction(int wholesale)
  {
    this.wholesale = wholesale;
  }

  public void addWholesale(int wholesale)
  {
    this.wholesale = wholesale;
  }

  public int getWholesale()
  {
    return this.wholesale;
  }
}
